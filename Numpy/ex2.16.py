# Using eye() function

import numpy as np

# Creating 2-D array with default value k=0
print(np.eye(2,4))
print('-'*50)

# Creating 2-D array with k=1
print(np.eye(5,k=1))
print('-'*50)

# Creating 2-D array with k=2
print(np.eye(5,k=2))
print('-'*50)

# Creating 2-D array with k=-1
print(np.eye(5,k=-1))
print('-'*50)

# Creating 2-D array with k=-2
print(np.eye(5,k=-2))


